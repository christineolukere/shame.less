// Helper function to get text with placeholder replacement
export function getQuizText(languageCode: string, key: string, placeholders?: Record<string, string | number>): string {
  const content = quizContent[languageCode as keyof typeof quizContent] || quizContent.en;
  let text = content[key as keyof typeof content] || key;
  
  if (placeholders) {
    Object.entries(placeholders).forEach(([placeholder, value]) => {
      text = text.replace(`{${placeholder}}`, String(value));
    });
  }
  
  return text;
}